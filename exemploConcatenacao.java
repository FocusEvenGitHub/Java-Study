public class exemploConcatenacao {
    public static void main(String[] args) {
        // Declaração de variáveis
        char letra = 'A';
        String palavra = "mazing";

        // Concatenação de char e String
        String mensagem = letra + palavra;

        // Imprime a mensagem concatenada
        System.out.println("A mensagem concatenada > " + mensagem);
    }
}
